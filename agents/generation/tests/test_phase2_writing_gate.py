def active_writing_debts(store: ProofStateStore) -> list[dict]:
    with sqlite3.connect(store.db_path) as conn:
        conn.row_factory = sqlite3.Row
        return [
            dict(row)
            for row in conn.execute(
                "SELECT * FROM debts WHERE debt_type='writing' AND status='active'"
            ).fetchall()
        ]


class Phase2WritingGateSchedulerTest(unittest.TestCase):
    def test_lightweight_gate_budget_constants(self) -> None:
        # The gate costs at most 4 LLM sessions in the worst case: authoring
        # (1) + deterministic-defect revision(s) + one editor review + at most
        # one editor-debt revision. These constants pin that design; the
        # deterministic allowance is separate from the editor's.
        self.assertEqual(1, MAX_WRITING_GATE_REVISION_CYCLES)
        self.assertEqual(2, MAX_WRITING_GATE_DETERMINISTIC_REVISION_CYCLES)
        self.assertEqual(1, PAPER_EDITOR_MAX_PASSES)
        self.assertEqual("editor", WRITING_GATE_EDITOR_LENS)
        self.assertNotEqual(WRITING_GATE_REVISION_INTENT, WRITING_GATE_DETERMINISTIC_REVISION_INTENT)

    def test_clean_certificate_dispatches_paper_authoring(self) -> None:
        # Certificate recorded, no final_paper yet -> immediate paper-authoring
        # write pass; never a review on the certificate, never stop_solved.
        with tempfile.TemporaryDirectory() as tmpdir:
            store = make_solved_store(Path(tmpdir), "writing-gate-authoring-test")
            attach_final_proof(store, "final-proof-1", CLEAN_FINAL_PROOF)

            action = next_action(store, web_search="disabled")

            self.assertEqual("write", action["mode"], action)
            self.assertTrue(action.get("paper_authoring"))
            self.assertEqual("final-proof-1", action["certificate_artifact_id"])
            self.assertEqual(WRITING_GATE_PAPER_INTENT, action["search_intent"])
            self.assertEqual("writer", actor_role_for_action(action))
            # The certificate is internal: no writing debts are created for it.
            self.assertEqual([], active_writing_debts(store))

    def test_certificate_with_lintable_content_is_never_linted(self) -> None:
        # The certificate is INTERNAL: even content that would trip the residue
        # scan produces no writing-lint debts and no certificate revision — the
        # gate goes straight to paper authoring. (The patch-time residue guard
        # on writer artifacts is a separate protection and still stands.)
        with tempfile.TemporaryDirectory() as tmpdir:
            store = make_solved_store(Path(tmpdir), "writing-gate-cert-unlinted-test")
            insert_final_proof_with_residue(
                store,
                "final-proof-residue",
                CLEAN_FINAL_PROOF.replace("QED.", "QED. TODO tighten the bound."),
            )

            action = next_action(store, web_search="disabled")

            self.assertEqual("write", action["mode"], action)
            self.assertTrue(action.get("paper_authoring"), action)
            self.assertFalse(action.get("writing_revision"), action)
            self.assertEqual("final-proof-residue", action["certificate_artifact_id"])
            self.assertEqual([], active_writing_debts(store))

    def test_certificate_compile_status_is_never_synced(self) -> None:
        # A certificate with a persisted failed compile gets no compile debt:
        # only the final_paper's pdf_status is gated.
        with tempfile.TemporaryDirectory() as tmpdir:
            store = make_solved_store(Path(tmpdir), "writing-gate-cert-compile-test")
            insert_final_proof_with_pdf_status(
                store,
                "final-proof-broken",
                CLEAN_FINAL_PROOF,
                pdf_status="compile_failed",
                latex_log="! Undefined control sequence.\nl.42 \\badmacro\n",
            )

            action = next_action(store, web_search="disabled")

            self.assertEqual("write", action["mode"], action)
            self.assertTrue(action.get("paper_authoring"), action)
            self.assertEqual([], active_writing_debts(store))

    def test_clean_paper_dispatches_exactly_one_editor_review(self) -> None:
        # Deterministically-clean paper -> the single editor exposition review.
        with tempfile.TemporaryDirectory() as tmpdir:
            store = make_solved_store(Path(tmpdir), "writing-gate-editor-dispatch-test")
            attach_final_proof(store, "final-proof-1", CLEAN_FINAL_PROOF)
            attach_final_paper(store, "final-paper-1", CLEAN_FINAL_PAPER)

            action = next_action(store, web_search="disabled")

