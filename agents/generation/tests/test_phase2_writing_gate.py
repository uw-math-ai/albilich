def insert_final_paper(
    store: ProofStateStore,
    artifact_id: str,
    content: str,
    *,
    pdf_status: str = "compiled",
    latex_log: str = "",
    created_at: str = "2026-01-04T00:00:00+00:00",
) -> None:
    """Insert a final_paper directly (skips guards and the pdflatex sidecar) so
    scheduler tests can shape the persisted compile status without compiling."""
    artifact_dir = store.state_dir / "artifacts"
    artifact_dir.mkdir(parents=True, exist_ok=True)
    path = artifact_dir / f"{artifact_id}.tex"
    path.write_text(content, encoding="utf-8")
    metadata: dict = {"certificate_artifact_id": "final-proof-1"}
    if pdf_status:
        metadata["pdf_status"] = pdf_status
    if latex_log:
        log_path = artifact_dir / f"{artifact_id}.latex.log"
        log_path.write_text(latex_log, encoding="utf-8")
        metadata["latex_log_path"] = str(log_path)
    with sqlite3.connect(store.db_path) as conn:
        conn.execute(
            "INSERT INTO artifacts(artifact_id, artifact_type, path, sha256, producer_role, run_id,"
            " state_revision, content_summary, metadata_json, created_at) VALUES (?,?,?,?,?,?,?,?,?,?)",
            (
                artifact_id,
                "final_paper",
                str(path),
                f"paper-{artifact_id}",
                "writer",
                "",
                store.get_revision(),
                "final paper",
                json.dumps(metadata),
                created_at,
            ),
        )
        conn.commit()


